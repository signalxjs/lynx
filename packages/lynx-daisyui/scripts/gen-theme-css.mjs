// Generate per-theme CSS classes for the built-in themes from the theme
// registry (the single source of truth). Emitting the palette as a real CSS
// *rule* (`.daisy-light { --color-*: … }`) is required because Lynx only
// resolves `var(--color-*)` on descendants when the variable is present at
// first paint — a custom property set later via the runtime `setProperty` API
// doesn't re-resolve already-painted descendants. Runtime-registered themes
// (no shipped CSS) still rely on `setProperty`, which works once they're
// selected post-mount. See `src/theme/ThemeProvider.tsx`.
//
// Runs after `tsgo` (so `dist/theme/registry.js` exists) and after
// `copy-assets` (so `dist/styles/themes/` exists). Output is a build artifact
// under `dist/` (gitignored); `src/styles/index.css` @imports it by name.
import { writeFileSync } from 'node:fs';
import { dirname, join } from 'node:path';
import { fileURLToPath, pathToFileURL } from 'node:url';

const here = dirname(fileURLToPath(import.meta.url));
// `pathToFileURL` so the dynamic import works on Windows too — Node's ESM
// loader rejects a raw absolute path there (`D:\…` parses as protocol `d:`).
const registryUrl = pathToFileURL(join(here, '../dist/theme/registry.js')).href;
const { listThemes } = await import(registryUrl);

const block = (t) => {
  const lines = Object.entries(t.colors).map(([k, v]) => `  --color-${k}: ${v};`);
  if (t.radius?.selector) lines.push(`  --radius-selector: ${t.radius.selector};`);
  if (t.radius?.field) lines.push(`  --radius-field: ${t.radius.field};`);
  if (t.radius?.box) lines.push(`  --radius-box: ${t.radius.box};`);
  return `.${t.name} {\n${lines.join('\n')}\n}`;
};

const themes = listThemes();
const css = `/* AUTO-GENERATED from src/theme/registry.ts — do not edit.\n`
  + ` * Regenerated by scripts/gen-theme-css.mjs on every build. */\n\n`
  + themes.map(block).join('\n\n') + '\n';

const out = join(here, '../dist/styles/themes/builtins.generated.css');
writeFileSync(out, css);
console.log(`gen-theme-css: wrote ${themes.length} built-in theme blocks → ${out}`);
